<?php

namespace App\Http\Controllers\Auth;

use App\Http\Controllers\Controller;
use App\Models\User;
use App\Providers\RouteServiceProvider;
use Illuminate\Auth\Events\Registered;
use Illuminate\Http\RedirectResponse;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;
use Illuminate\Support\Facades\Hash;
use Illuminate\Validation\Rules;
use Illuminate\View\View;

class RegisteredUserController extends Controller
{
    /**
     * Display the registration view.
     */
    public function create(Request $request): View
    {
        // Simpan info redirect (admin/wartawan) di session
        if ($request->has('redirect')) {
            session(['redirect_after_login' => $request->get('redirect')]);
        }

        return view('auth.register');
    }

    /**
     * Handle an incoming registration request.
     */
    public function store(Request $request): RedirectResponse
    {
        $request->validate([
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'email', 'max:255', 'unique:' . User::class],
            'password' => ['required', 'confirmed', Rules\Password::defaults()],
        ]);

        $user = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
        ]);

        event(new Registered($user));

        Auth::login($user);

        // Ambil nilai redirect dari session
        $redirect = session('redirect_after_login');

        // Redirect ke dashboard sesuai tombol
        if ($redirect === 'admin') {
            return redirect('/admin/dashboard');
        } elseif ($redirect === 'wartawan') {
            return redirect('/wartawan/dashboard');
        }

        // Default
        return redirect('/dashboard');
    }
}
